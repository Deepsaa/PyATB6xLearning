# Task for the day
# Take 3 number from user
# Perform sum,sub,mul,div task
num1 = float(input("Enter a number: "))
num2 = float(input("Enter another number: "))
num3 = float(input("Enter a third number: "))
# Performing operation
sum_result = num1 + num2 + num3 #adding one by one number
sub_result = num1 - num2 - num3 #Substracting 2nd number from first
mul_result = num1 * num2 * num3 #Multiplying 1st number with 2nd nmbr
div_result = num1 / num2 / num3
print(sum_result)
print(sub_result)
print(mul_result)
print(div_result)
